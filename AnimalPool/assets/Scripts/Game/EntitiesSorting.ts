const {ccclass, property} = cc._decorator;

@ccclass
export default class EntitiesSorting extends cc.Component {
    update(dt)
    {
        let sortedArray = Array.from(this.node.children).sort((a, b) => -a.y + b.y);
        let depth = 0;
        sortedArray.forEach(node =>{
            node.zIndex = depth;
            depth++;
        });
    }
}
